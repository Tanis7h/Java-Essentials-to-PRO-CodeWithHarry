package CWH_Chapter_05;

public class cwh_ch5_ps_pr07 {
    public static void main(String[] args) {
        int i=0;
        while (i<5) {
            int j=i;
            while (j<5) {
                System.out.print("*");
                j++;
            }
            System.out.print("\n");
            i++;
        }
    }
}
